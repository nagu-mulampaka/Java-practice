// Program: Demonstrating Relational Operators Operators in Java
class RelationalOperators {
    public static void main(String args[])
    {
        int a = 25;
        int b = 39;

        System.out.println(" --- Relational Operators Example --- ");
        // Equal to (==), o/p: false
        System.out.println("a==b: "+(a==b));

        // Not Equal to(!=) o/p: true
        System.out.println("a!=b: "+(a!=b));

        // Greater than (>) o/p: false
        System.out.println("a>b: "+(a>b));

        // Greater than or Equal to (>=) o/p: false
        System.out.println("a>=b: "+(a>=b));

        // Less then(<) o/p: ture
        System.out.println("a<b: "+(a<b));

        // Less then or Equal to (<=) o/p: ture
        System.out.println("a<=b: "+(a<=b));
    }
}